public class Quotes {
    private String commod;
    private int quant, rid;
    private float price;

    public String getCommod() {
        return commod;
    }

    public void setCommod(String commod) {
        this.commod = commod;
    }

    public int getQuant() {
        return quant;
    }

    public void setQuant(int quant) {
        this.quant = quant;
    }

    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
